export interface ColorPalette {
  50: Color;
  100: Color;
  200: Color;
  300: Color;
  400: Color;
  500: Color;
  600: Color;
  700: Color;
  800: Color;
  900: Color;
  950: Color;
}

/**
 * Hexadecimal color string with the sharp, e.g. #FFFFFF
 */
export type Color = string;

export type ValidColorSaturation = keyof ColorPalette;

export function getColor(
  color: ColorPalette,
  saturation: ValidColorSaturation,
  opposite?: boolean
): Color {
  const saturations: ValidColorSaturation[] = [
    50, 100, 200, 300, 400, 500, 600, 700, 800, 900, 950,
  ];
  if (opposite) {
    const index = saturations.indexOf(saturation);
    const oppositeIndex = saturations.length - 1 - index;
    const oppositeSaturation = saturations[oppositeIndex];
    return color[oppositeSaturation];
  } else {
    return color[saturation];
  }
}

